from django.contrib import admin
#--------------------------------------------------------------------------------

#--------------------------------------------------------------------------------
#--------------------------------------------------------------------------------
# Import necessary Models
#--------------------------------------------------------------------------------
from personalmanagement.processlist.models import ProcessList
#--------------------------------------------------------------------------------

#--------------------------------------------------------------------------------
#--------------------------------------------------------------------------------
# Admin
#--------------------------------------------------------------------------------
class ProcessListAdminInline(admin.StackedInline):
    model = ProcessList
    fk_name = 'user'
    fieldsets = (
        (None, {'fields':(
            ('user'),
            )}),
        ('Datum', {'fields':(
            ('begin','end'),
            )}),
        ('Tages', {'fields':(
            'day',
            )}),
        ('Url', {'fields':(
            'url',
            )}),
        ('Anzeigen', {'fields':(
            ('id_show'),
            ('referenceNumber_show'),
            ('name_show'),
            ('picture_show'),
            ('technicalDataSheet_show'),
            ('update_show'),
            ('delete_show'),
            )}),
    )
    filter_horizontal = []
