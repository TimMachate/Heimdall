#--------------------------------------------------------------------------------
# Import necessary Moduls
#--------------------------------------------------------------------------------
from django.db import models
from django.utils.translation import gettext_lazy as _
#--------------------------------------------------------------------------------

#--------------------------------------------------------------------------------
#--------------------------------------------------------------------------------
# Import necessary Models
#--------------------------------------------------------------------------------
from .abstract import CreateData
#--------------------------------------------------------------------------------

#--------------------------------------------------------------------------------
#--------------------------------------------------------------------------------
# Model
#--------------------------------------------------------------------------------
class BatchProcess(CreateData):

    name = models.CharField(
        name = 'name',
        verbose_name = 'Name',
        max_length = 200,
        blank = False,
        null = False,
    )

    commission = models.ManyToManyField(
        name = 'commission',
        verbose_name = 'Auftrag',
        to = 'commission.Commission',
        blank = True,
        null = True,
    )

    class Meta:
        verbose_name = 'Batch Prozess'
        verbose_name_plural = 'Batch Prozesse'
